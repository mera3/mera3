#include <unistd.h>

void	ft_prnt_alph(void)
{
	int i;

	i = 97;
	while (i <= 122)
	{
		write(1, &i, 1);
		i++;
	}
}

int		main(void)
{
	ft_prnt_alph();
	return (0);
}
