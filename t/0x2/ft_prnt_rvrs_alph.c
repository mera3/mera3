#include <unistd.h>

void	ft_prnt_rvrs_alph(void)
{
	int i;

	i = 122;
	while (i >= 97)
	{
		write(1, &i, 1);
		i--;
	}
}

int		main(void)
{
	ft_prnt_rvrs_alph();
	return (0);
}
