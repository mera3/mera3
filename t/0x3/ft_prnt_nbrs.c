#include <unistd.h>

void	ft_prnt_nbrs(void)
{
	int i;

	i = '1';
	while (i <= '9')
	{
		write(1, &i, 1);
		i++;
	}
}

int		main(void)
{
	ft_prnt_nbrs();
	return (0);
}
